#include "../includes/cub3d.h"

void			init_vector(t_pos *to, int x, int y)
{
	to->x = x;
	to->y = y;
}

void			init_vector_arrow(int dx[4], int dy[4])
{
	dx = (int[4]){1, -1, 0, 0};
	dy = (int[4]){0, 0, -1, 1};
}
